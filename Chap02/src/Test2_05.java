
public class Test2_05 {

	public static void main(String[] args) {
		int aaa; //변수 선언
		aaa = 10; //write(메모리에 aaa변수 저장공간에 10이라는 수를 씀), 변수의 초기화
		
		System.out.println(aaa); //read 메모리에서 변수 aaa에 저장되어있는 저장값을 읽음)
		
		aaa=20;//write (메모리에 aaa변수 저장공간에 저장되어있는 10을 20으로 다시 씀), 변수의 초기화
		
		System.out.println(aaa); //read
	}

}
