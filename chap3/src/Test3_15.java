
public class Test3_15 {

	public static void main(String[] args) {
		//비교 연산자, true와 false를 반환
		
		int a = 7;
	
		boolean test = a > 10; // a가 10보다 큰가? , false
		test = a >= 10; // a가 10보다 크거나 같은가?, false
		test = a < 10; //a가 10보다 작은가? , true
		test = a <= 7; // a가  7보다 작거나 같은가? , true
		
		test = a == 10; //두항이 같은가?, false
		test = a != 10; //두항이 다른가?, true
	}
}
