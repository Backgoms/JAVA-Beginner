
public class Test3_06 {
	public static void main(String [] args) {
		//증감 연산자 ++, --
		//일반적으로 오른쪽으로 붙임
		int aaa1 = 3;
		System.out.println(aaa1);
	
		aaa1++;
		System.out.println(aaa1);
	
		aaa1++;
		System.out.println(aaa1);

		aaa1--;
		System.out.println(aaa1);
		
		++aaa1;
		System.out.println(aaa1);
		--aaa1;
		System.out.println(aaa1);
	}
}